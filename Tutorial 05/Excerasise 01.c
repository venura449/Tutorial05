#include<stdio.h>

int main()
{
    int i = 0; // Initialize a variable 'i' with the value 0

    // Use a loop to iterate from 10 to 20 (inclusive)
    for(i = 10; i <= 20; i++)
    {
        printf("%d\n", i); // Print the value of 'i' followed by a newline
    }

    return 0; // Indicate successful completion of the program
}
